#include <stdio.h>

int main()
{
    int a = 20, b = 10;

    printf("First Number  : %d\n", a);
    printf("Second Number : %d\n\n", b);

    printf("a == b : %d\n", a == b);
    printf("a != b : %d\n", a != b);
    printf("a > b  : %d\n", a > b);
    printf("a < b  : %d\n", a < b);
    printf("a >= b : %d\n", a >= b);
    printf("a <= b : %d\n", a <= b);

    return 0;
}
